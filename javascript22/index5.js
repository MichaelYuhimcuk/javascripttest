// Завдання шосте
let number = 10;
let text = "20";
let result = number + text;
console.log(result); 

console.log(5 == "5");  // true - == приводить рядок "5" до числа 5
console.log(5 === "5"); // false - === не виконує приведення типів

// Пояснення:
// У першому випадку (5 == "5") рядок "5" автоматично приводиться до числа 5, тому порівняння дає true.
// У другому випадку (5 === "5") порівнюються не тільки значення, а й типи, тому результат - false.